/*
  Interpret word as a base-26 number.

  Note: A real ord() function should produce unique values.
  Here, we are only using 64 bits and there are infinitely
  many words. So this is not true here.

  But, since we use only it for hashing. This is not an issue.
*/
uint64_t ord(const char *word)
{
    uint64_t p = 1;
    uint64_t k = 0;

    while (*word) {
        k = (*word - 'a') + p * k;
        p *= 26;
        word++;
    }

    return k;
}

uint64_t hash(uint64_t key)
{
    /* Choose a prime around 4000. For ~ 1 entry per list. */
    uint64_t p = 4001;
    uint64_t a = 5; /* `a` should not be 0. */
    uint64_t b = 11;
    return (a * key + b) % p;
}

typedef uint64_t u64;

template <typename K, typename V>
struct hashnode
{
    K k;
    V v;
    hashnode<K, V>* next;
};

template <typename K, typename V>
hashnode<K, V> *new_node(K k, V v)
{
    auto n = new hashnode<K, V>;
    n->k = k;
    n->v = v;
    n->next = NULL;
    return n;
}

template <typename K, typename V>
struct hashmap
{
    u64 a, b, p;
    u64 n;
    std::vector<hashnode<K, V>*> slots;
};

bool is_prime(u64 p)
{
    if (p % 2 == 0) return false;
    for (u64 d = 3; d*d <= p; ++d) {
        if (p % d == 0) return false;
    }
    return true;
}

u64 random_int(u64 start, u64 end)
{
    std::random_device rd;
    std::mt19937 gen(rd());
    std::uniform_int_distribution<> dist(start, end);
    return dist(gen);
}

u64 random_prime(u64 start, u64 end)
{
   u64 p;

   while (!is_prime(p = random_int(start, end))) {}
   return p;
}

template <typename K, typename V>
void rehash(hashmap<K, V>& h)
{
    u64 p = random_prime(h.n, 2*h.n);
    u64 a = random_int(1, p-1);
    u64 b = random_int(0, p-1);
    std::vector<hashnode<K, V>*> old_slots = std::move(h.slots);
    h.slots.resize(p);

    for (u64 i = 0; i < h.p; ++i) {
        auto x = old_slots[i];
        while (x) {
            auto y = x->next;
            auto new_hash = (a*ord(x->k) + b) % p;
            x->next = h.slots[new_hash];
            h.slots[new_hash] = x;
            x = y;
        }
    }
    h.p = p;
    h.a = a;
    h.b = b;
}

template <typename K, typename V>
hashmap<K, V> empty()
{
    hashmap<K, V> h;
    h.n = 0;
    h.a = 3;
    h.b = 5;
    h.p = 19;
    for (int i = 0; i < h.p; ++i) {
        h.slots.push_back(NULL);
    }
    return h;
}

template <typename K, typename V>
void insert(hashmap<K, V>& h, K k, V v)
{
    u64 hash = (h.a * ord(k) + h.b) % h.p;

    auto node = new_node(k, v);

    node->next = h.slots[hash];
    h.slots[hash] = node;
    ++h.n;

    if (2*h.p < h.n) {
        rehash(h);
    }
}

template <typename K, typename V>
bool member(const hashmap<K, V>& h, K k)
{
    u64 hash = (h.a * ord(k) + h.b) % h.p;
    auto p = h.slots[hash];

    while (p) {
        if (p->k == k) {
            return true;
        }
        p = p->next;
    }
    return false;
}

template <typename K, typename V>
V lookup(const hashmap<K, V>& h, K k)
{
    u64 hash = (h.a * ord(k) + h.b) % h.p;
    auto p = h.slots[hash];

    while (p) {
        if (p->k == k) {
            return p->v;
        }
        p = p->next;
    }
    assert(0);
}

template <typename K, typename V>
void print_hashnodes(const hashnode<K, V> *h)
{
    while (h) {
        printf("(%s, %d) -> ", h->k, h->v);
        h = h->next;
    }
    printf("NULL\n");
}

template <typename K, typename V>
void print_hashmap(const hashmap<K, V>& h)
{
    printf("(%lu, %lu, %lu)\n", h.a, h.b, h.p);

    for (int i = 0; i < h.p; ++i) {
        printf("%d => ", i);
        print_hashnodes(h.slots[i]);
    }
}

int main()
{
    auto x = empty<const char *, int>();
    print_hashmap(x);

    insert(x, "es242", 25);
    print_hashmap(x);

    insert(x, "es112", 20);
    print_hashmap(x);

    assert(member(x, "es112"));
    assert(!member(x, "cs222"));

    insert(x, "abc", 33);
    insert(x, "abc", 34);
    print_hashmap(x);
    printf("%d\n", lookup(x, "abc"));

    for (int i = 0; i < 4096; ++i) {
        insert(x, words[i], (int)random_int(1, 100));
    }

    print_hashmap(x);

    return 0;
}
